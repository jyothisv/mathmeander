/// Durable workspace identity of an object (arch doc В§6.0b: `object.type` в‰  `unit.type`).
/// Slice 1 widens this from the skeleton's lone `note` to the full В§6 vocabulary: the
/// formal family (theorem/lemma/proposition/corollary/conjecture/claim), definition,
/// proof, example, question вЂ” all producible via create вЂ” plus `journal_day`, producible
/// since slice 2b via its В§6.5 surface (not the plain typed POST). `source_excerpt`, `trail`,
/// and `annotation` remain **reserved vocabulary** (valid on read, not yet producible вЂ” their
/// detail tables / surfaces land in later slices, В§6.1a/В§13a).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[cfg_attr(feature = "schema-artifact", derive(schemars::JsonSchema))]
#[serde(rename_all = "snake_case")]
pub enum ObjectType {
    Note,
    // The formal family (enum-only; no detail tables вЂ” В§6.0b/В§6.1c).
    Theorem,
    Lemma,
    Proposition,
    Corollary,
    Conjecture,
    Claim,
    Definition,
    Proof,
    Example,
    Question,
    // Reserved vocabulary вЂ” known on read, not producible via the plain create path
    // until their detail tables / surfaces arrive (source: В§13a.3, annotation: В§13a.4,
    // trail: В§6.5 surfaces, slice 2+). The producibility gate is in `validate`.
    SourceExcerpt,
    Trail,
    Annotation,
    // journal_day BECAME producible in slice 2b вЂ” via its own surface
    // (`validate::create_journal_day` + `journal_day_detail`), NOT the plain typed POST; it
    // stays non-directly-creatable (a raw POST still 422s with TypeNotDirectlyCreatable). (В§6.5)
    JournalDay,
    // notebook вЂ” a structured WRITING-SURFACE (В§6.5 / В§B), the non-date sibling of journal_day:
    // produced via its own surface op (`validate::create_notebook` + `notebook_detail`), identity is
    // its per-space `slug`. Like journal_day it is producible-but-not-directly-creatable, and is a
    // SURFACE (so never a greedy-capture/copy target). Authored into directly with В§B `Heading` sections.
    Notebook,
}

impl ObjectType {
    /// Whether the plain create path may PRODUCE this type. The reserved surface /
    /// source / annotation types are valid vocabulary (they parse on read) but their
    /// owning machinery (detail tables, surfaces) lands in later slices, so creating one
    /// now would leave a type-qualified-reference invariant unsatisfiable (В§6.1a).
    pub fn is_producible(self) -> bool {
        !matches!(
            self,
            ObjectType::SourceExcerpt | ObjectType::Trail | ObjectType::Annotation
        )
    }

    /// Whether a client may mint this type via the DIRECT typed-create path. Only `Note`
    /// (the freeform default) вЂ” the formal family enters the graph by *declaration* (the user
    /// types `Thm.`/`Def:`) в†’ materialization (В§9.y/В§13a slice 2), never a raw typed POST.
    /// This is strictly NARROWER than `is_producible`: the family IS producible (it is the
    /// output of `materialize_object` and round-trips through `.mathpack` import, and is
    /// numbered/exported) вЂ” it is only the direct-create *surface* that is gated.
    pub fn is_directly_creatable(self) -> bool {
        matches!(self, ObjectType::Note)
    }

    /// Whether this is a В§6.5 SURFACE вЂ” a dated/ordered writing context (`journal_day`; `trail`
    /// later), not a materializable knowledge object. Surfaces are created via their own surface op
    /// (`create_journal_day`), NEVER as the output of greedy capture (`rehome_subtree`) or the copy
    /// path вЂ” so they are excluded as materialization targets even when producible. This is what
    /// keeps the `is_producible` lift (slice 2b) from silently opening `journal_day` as a rehome
    /// target (which would mint a dateless, detail-less day, bypassing `UNIQUE(space_id, date)`).
    pub fn is_surface(self) -> bool {
        matches!(
            self,
            ObjectType::JournalDay | ObjectType::Trail | ObjectType::Notebook
        )
    }
}

