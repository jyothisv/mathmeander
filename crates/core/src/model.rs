// В§2.2 foreign-field policy вЂ” deliberately ASYMMETRIC with `CanonicalObject` above.
// Content-level types (`Unit`, `UnitContent`, `Inline`, `MathExpression`) carry no `flatten`
// catch-all and no `deny_unknown_fields`, so serde DROPS unknown fields on parse. This is
// intentional: unknown-field *preservation* (В§2.2) is scoped to the `CanonicalObject` envelope вЂ”
// the cross-version import boundary вЂ” not to in-version content authored through the strict
// `save_content` reconcile gate (`ops.rs`). Dropping is the safe default: a dropped field is never
// persisted, so it cannot be smuggled past the gate or bloat storage. Revisit only when a real
// cross-version content-import path (`.mathpack`) makes lossless content forward-compat load-bearing.

/// A content unit вЂ” the В§6.0b `content_units` row. Content is the authored material;
/// `content_kind` is a DERIVED SQL column (the `content` tag), so it is NOT a field here.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[cfg_attr(feature = "schema-artifact", derive(schemars::JsonSchema))]
pub struct Unit {
    pub id: UnitId,
    pub object_id: ObjectId,
    /// THE nesting mechanism: a child names its parent (must be in the same object, В§6.1a).
    pub parent_unit_id: Option<UnitId>,
    /// Order among siblings (В§6.0b).
    pub position: u32,
    /// Container-internal part where needed, e.g. `assumption` (case child) /
    /// `justification` (proof_step child); `None` otherwise.
    pub slot: Option<String>,
    /// The relation this row asserts toward its prior sibling вЂ” a `Derivation` step's connective or
    /// an `Equations` row's leading relation; `None` otherwise (the common `Equations` case).
    pub row_relation: Option<RowRelation>,
    /// THE one user-facing math-flow label; `None` = plain content.
    #[serde(rename = "type")]
    pub unit_type: Option<UnitType>,
    /// For `type = example`: the example refinement.
    pub example_kind: Option<ExampleKind>,
    pub status: UnitStatus,
    pub declared_by: DeclaredBy,
    /// Optional candidate decomposition вЂ” declared, never written in slice 1 (В§6.0).
    pub extracted_structure: Option<ExtractedStructureEnvelope>,
    pub content: UnitContent,
    pub provenance_id: ProvenanceId,
}

/// An edge вЂ” the В§6.1b `links` row. Slice 1 supports a TWO-ARM target (`target_object_id`
