/// An inline element of prose (В§6.0): minimal formatting, inline math, or a mention.
///
/// **The span contract** вЂ” `span` always char-indexes into the prose `text`, but two shapes:
///   вЂў An element that carries its **own content field** is a ZERO-WIDTH ATOM (`span = [p, p]`):
///     its surface lives in that field, NOT in the prose `text`, so there is a single source of
///     truth (В§2.2 вЂ” no `Reference.text`-vs-`prose[span]` drift). `Math` (content in `expr`) and
///     `Reference` (content in `text`) are atoms; the prose `text` holds no characters for them.
///   вЂў `Mark` carries no content вЂ” it is a REGION `[s, e)` overlaying real prose `text`
///     (formatting). It may be non-empty.
/// Inline `$вЂ¦$` math is thus a positional atom, not its own unit (else one sentence explodes into
/// units); promoting it to display math (`$$`) is `ops::toggle_expression_placement`. The
/// zero-width invariant for atoms is enforced by `validate::validate_inline` (not just convention),
/// so a future op/editor cannot silently reintroduce width. Rich formatting waits for the editor
/// (slice 2); MVP `Mark` carries an open-ended `style` tag so import/export round-trips losslessly.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[cfg_attr(feature = "schema-artifact", derive(schemars::JsonSchema))]
#[serde(tag = "kind", rename_all = "snake_case")]
pub enum Inline {
    /// A formatting region over prose `text` вЂ” `span` may be non-empty (the marked range).
    Mark { span: CharSpan, style: String },
    /// Inline math вЂ” a zero-width atom (`span = [p, p]`); content lives in `expr`.
    Math {
        span: CharSpan,
        expr: MathExpression,
    },
    /// A mention вЂ” a zero-width atom (`span = [p, p]`); the surface lives in `text`.
    Reference {
        span: CharSpan,
        /// The literal reference surface ("BolzanoвЂ“Weierstrass") вЂ” kept verbatim; the
        /// resolved edge lives in `links` (В§6.1b).
        text: String,
        target: Option<ReferenceTarget>,
    },
}

/// A SINGLE unit's own authored material (В§6.0) вЂ” never an embedded unit tree (children
/// are rows: `parent_unit_id` + `position` + `slot`). The internal `kind` tag is the
/// AUTHORITATIVE discriminator and the source of the derived `content_kind` SQL column.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
#[cfg_attr(feature = "schema-artifact", derive(schemars::JsonSchema))]
#[serde(tag = "kind", rename_all = "snake_case")]
pub enum UnitContent {
    /// Prose + inline formatting + inline math + mentions.
    Prose { text: String, inline: Vec<Inline> },
    /// A display/block PLACEMENT of one `MathExpression` вЂ” not a separate math model.
    Math { expr: MathExpression },
    /// Items are child rows.
    List { ordered: bool },
    /// Steps are child rows; each step child carries its relation (slice 1b+).
    Derivation,
    /// Co-equal, jointly-asserted relation rows вЂ” the "system of equations" `{2x+y=1; xв€’y=4}`.
    /// Children are `Math`/`Prose` rows (`parent_unit_id` в†’ this container); each row may carry a
    /// `row_relation`. Names the MATHEMATICS, not the layout вЂ” column alignment is DERIVED at render
    /// (never stored). Distinct from `Derivation` (sequential) and `CaseSplit` (conditional).
    Equations,
    /// Each case is a child group; `slot` marks the assumption child vs body children.
    CaseSplit,
    /// THE generic container вЂ” a typed unit's multi-part body, or a neutral grouping.
    Group,
    /// A titled section anchor (В§B). The title IS this unit's own prose вЂ” `text` + `inline`, with the
    /// full prose vocabulary (formatting `Mark`s, inline `Math` atoms, `Reference` mentions), so a
    /// heading can read "В§3 Properties of $L^2$". The section's BODY and SUBSECTIONS are this unit's
    /// CHILD rows (`parent_unit_id` в†’ this heading); a subsection is just a child `Heading`. Because the
    /// title lives ON the unit (not in a child), every section unit is 1:1 with one editor block вЂ” the
    /// flat-flush invariant вЂ” and a section move is a single `reparent_unit` (the subtree follows). LEVEL
    /// is nesting depth (no level field, В§B). Distinct from the anonymous `Group`: a `Heading` is titled
    /// and participates in the outline / numbering. The title edits freely through `save_content` (like
    /// `Prose`); becoming/un-becoming a heading is the `toggle_heading` op (a content-KIND change, frozen
    /// from the coarse delta like every other kind transition).
    Heading { text: String, inline: Vec<Inline> },
    /// Quotes & object embeds.
    Embed { target: EmbedTarget },
    /// A config-family block (the notation home, В§Design-model): declarative `source` interpreted by
    /// `family`. The `source` is the canonical, lossless truth; any rendering/resolution is DERIVED
    /// (notation-as-register вЂ” the literal source is never rewritten). A leaf (never a `parent_unit_id`);
    /// its `source` edits freely through `save_content` like `Prose`, but a kind flip into/out of `Config`
    /// is rejected like every other content-kind transition.
    Config {
        family: ConfigFamily,
        source: String,
    },
}

/// The declarative category of a `UnitContent::Config` block вЂ” it selects the interpreter that reads the
/// block's `source` (notation в†’ a render-time register; conventions / annotation-recipes / styles join
/// later as added variants, reusing the one block construct). Wire vocabulary is the variant name
/// (`"notation"`). A new family is an ADDITIVE enum variant + its own interpreter вЂ” the block / editor /
/// projection machinery is shared, the cascade is a property of config-family blocks.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[cfg_attr(feature = "schema-artifact", derive(schemars::JsonSchema))]
#[serde(rename_all = "snake_case")]
pub enum ConfigFamily {
    /// User-defined notation: `TRIGGER := EXPANSION` lines, resolved at render (notation-as-register).
    Notation,
}

/// The typed relation a row asserts relative to its prior sibling row (a `Derivation` step's
/// connective, or an `Equations` row's leading relation). `None` = the row's relation is intrinsic
/// to its own content (the common `Equations` case) or none. The domain is `вЉ†` the surface grammar's
/// relation set (`SYMBOLIC_RELATIONS в€Є WORD_RELATIONS`), kept in sync by a mechanical test. It is
/// PRESENTATION-FREE: render may show its glyph in a gutter / alignment column, but layout is never
/// stored. Wire vocabulary is the variant name (`"eq"`), not the symbol; `token()` bridges to the
/// grammar token.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[cfg_attr(feature = "schema-artifact", derive(schemars::JsonSchema))]
#[serde(rename_all = "snake_case")]
pub enum RowRelation {
    Eq,
    Lt,
    Gt,
    Le,
    Ge,
    Ne,
    Defines,
    MapsTo,
    Implies,
    In,
    NotIn,
    Subset,
    Subseteq,
}

impl RowRelation {
    /// The grammar relation TOKEN this variant denotes. The sync test asserts the image of this map
    /// over every variant equals `grammar::SYMBOLIC_RELATIONS в€Є grammar::WORD_RELATIONS`. The
    /// non-wildcard match forces a token decision when a variant is added.
    pub fn token(self) -> &'static str {
        match self {
            RowRelation::Eq => "=",
            RowRelation::Lt => "<",
            RowRelation::Gt => ">",
            RowRelation::Le => "<=",
            RowRelation::Ge => ">=",
            RowRelation::Ne => "!=",
            RowRelation::Defines => ":=",
            RowRelation::MapsTo => "->",
            RowRelation::Implies => "=>",
            RowRelation::In => "in",
            RowRelation::NotIn => "notin",
            RowRelation::Subset => "subset",
            RowRelation::Subseteq => "subseteq",
        }
    }

    /// Every variant, in declaration order вЂ” used by the grammar sync test (Rust has no built-in
    /// enum iteration and adding `strum` would violate the pure-core dependency minimalism).
    pub const ALL: [RowRelation; 13] = [
        RowRelation::Eq,
        RowRelation::Lt,
        RowRelation::Gt,
        RowRelation::Le,
        RowRelation::Ge,
        RowRelation::Ne,
        RowRelation::Defines,
        RowRelation::MapsTo,
        RowRelation::Implies,
        RowRelation::In,
        RowRelation::NotIn,
        RowRelation::Subset,
        RowRelation::Subseteq,
    ];
}

